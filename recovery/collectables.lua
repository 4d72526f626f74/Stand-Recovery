    script:add(
        script.casino:toggle("Show All Cards", {}, "Adds a blip showing the location of each card on the map", function(state)
            if state then
                script:enumerate(collectables.casino.card_locations, function(key, value)
                    local vector = value
                    local blip = HUD.ADD_BLIP_FOR_COORD(vector.x, vector.y, vector.z)
                    HUD.SET_BLIP_SPRITE(blip, 680)
        
                    table.insert(collectables.blips.casino, blip)
                end)
            else
                script:enumerate(collectables.blips.casino, function(key, value)
                    local blip = value
                    util.remove_blip(blip)
                end)
            end
        end),
        "casino_show_all_cards"
    )

    script:add(
        script.casino:toggle_loop("Collect Nearby Cards", {}, "Automatically collect casino cards when they\'re within range", function()
            local pos = ENTITY.GET_ENTITY_COORDS(script.me_ped, true)

            script:enumerate(entities.get_all_pickups_as_handles(), function(key, pickup)
                if ENTITY.DOES_ENTITY_EXIST(pickup) then
                    local model = util.reverse_joaat(ENTITY.GET_ENTITY_MODEL(pickup))

                    if model:find("vw_prop_vw_lux_card_01a") then
                        ENTITY.SET_ENTITY_COORDS(pickup, pos.x, pos.y, pos.z, true, true, true, true)
                    end
                end
            end)
        end),
        "casino_collect_all_cards"
    )

    script:add(
        script.casino:action("Teleport To Cards", {}, "Teleports you to each card location and collects it for you", function()
            local ref = menu.ref_by_rel_path(script.casino, "Collect Nearby Cards")

            if script:STAT_GET_INT("PLAYING_CARD_COLLECTED") >= 54 then
                util.toast("You already have all the cards")
                util.stop_thread()
            end

            ref.value = true

            script:enumerate(collectables.casino.card_locations, function(key, value)
                local vector = value

                ENTITY.SET_ENTITY_COORDS(script.me_ped, vector.x, vector.y, vector.z, true, true, true, true)
                util.toast("Collecting card " .. key .. " of 54")
                util.yield(3000)
            end)

            ref.value = false
        end),
        "casino_teleport_to_cards"
    )

